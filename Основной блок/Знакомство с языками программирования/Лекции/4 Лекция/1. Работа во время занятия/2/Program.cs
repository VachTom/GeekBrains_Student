void PrintImage(int[,] image)
{
    for (int x = 0; x < image.GetLength(0); x++)
    {
        for (int y = 0; y < image.GetLength(1); y++)
        {
            // Console.Write($"{image[x, y]} ");
            if (image[x, y] == 0) Console.Write($" ");
            else Console.Write($"1");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row-1, col);
        FillImage(row+1, col);
        FillImage(row, col-1);
        FillImage(row, col+1);
    }
}


